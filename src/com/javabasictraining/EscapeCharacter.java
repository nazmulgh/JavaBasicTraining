package com.javabasictraining;

public class EscapeCharacter {

    public static void main(String[] args) {

        System.out.println("I am learning Java \nI am learning JavaScript");
        System.out.println("I am learning Java \n I am learning JavaScript");

        System.out.println("I am learning Java \tI am learning JavaScript");
        System.out.println("I am learning Java \t I am learning JavaScript");

        System.out.println("I am learning Java \bI am learning JavaScript");
        System.out.println("I am learning Java \b I am learning JavaScript");

        System.out.println("I am learning Java \"I am learning JavaScript\"");


    }


}

